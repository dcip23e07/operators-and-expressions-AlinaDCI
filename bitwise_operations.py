#1. convert 4999 to base 2 using python

binary_num1 = bin(4999)
print (binary_num1) # Result: 0b1001110000111

#2. convert 2111 to base 2 using python
binary_num2 = bin(2111)
print (binary_num2) # Result: 0b100000111111

#3. what will be the value of 4999 & 2111
value1 =  4999 & 2111
print ("the value of 4999 & 2111: ", value1) # Result: 7

#4. what will be the value of 4999 | 2111
value2 =  4999 | 2111
print ("the value of 4999 | 2111: ", value2) # Result: 7103